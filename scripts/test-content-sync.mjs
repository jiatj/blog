const defaultUrl = process.env.PORTAL_SYNC_URL ?? "http://localhost:3000/api/content/sync";
const defaultSecret = process.env.CONTENT_SYNC_SECRET ?? "";

function parseArgs(argv) {
  const options = {
    url: defaultUrl,
    secret: defaultSecret,
    sourcePath: "external/demo-post.md",
    dryRun: false,
    deleteOnly: false,
    title: "Portal Sync Demo",
    slug: "portal-sync-demo",
    summary: "A manual test payload for portal content sync.",
    locale: "zh",
    contentType: "post",
    date: "2026-03-27"
  };

  for (let index = 0; index < argv.length; index += 1) {
    const current = argv[index];
    const next = argv[index + 1];

    switch (current) {
      case "--url":
        options.url = next;
        index += 1;
        break;
      case "--secret":
        options.secret = next;
        index += 1;
        break;
      case "--path":
        options.sourcePath = next;
        index += 1;
        break;
      case "--title":
        options.title = next;
        index += 1;
        break;
      case "--slug":
        options.slug = next;
        index += 1;
        break;
      case "--summary":
        options.summary = next;
        index += 1;
        break;
      case "--locale":
        options.locale = next;
        index += 1;
        break;
      case "--type":
        options.contentType = next;
        index += 1;
        break;
      case "--date":
        options.date = next;
        index += 1;
        break;
      case "--dry-run":
        options.dryRun = true;
        break;
      case "--delete":
        options.deleteOnly = true;
        break;
      default:
        break;
    }
  }

  return options;
}

function buildMarkdown(options) {
  return `---
title: ${options.title}
slug: ${options.slug}
summary: ${options.summary}
locale: ${options.locale}
contentType: ${options.contentType}
syncToPortal: true
date: ${options.date}
---

This document is generated by \`scripts/test-content-sync.mjs\`.
`;
}

async function main() {
  const options = parseArgs(process.argv.slice(2));
  const payload = {
    provider: "manual",
    dryRun: options.dryRun,
    files: options.deleteOnly
      ? []
      : [
          {
            path: options.sourcePath,
            content: buildMarkdown(options)
          }
        ],
    deletedPaths: options.deleteOnly ? [options.sourcePath] : []
  };

  const response = await fetch(options.url, {
    method: "POST",
    headers: {
      "content-type": "application/json",
      ...(options.secret ? { "x-portal-secret": options.secret } : {})
    },
    body: JSON.stringify(payload)
  });

  const text = await response.text();
  console.log(text);

  if (!response.ok) {
    process.exitCode = 1;
  }
}

main().catch((error) => {
  console.error(error);
  process.exitCode = 1;
});
